    public SecApartmentWindow()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);

        _sprite = _entitySystem.GetEntitySystem<SpriteSystem>();
        _gameTicker = _entitySystem.GetEntitySystem<ClientGameTicker>();
        _styles = new SecApartmentStyles(_resCache);
        _previousStylesheet = _ui.Stylesheet;

        SetupAllStyles();

        OnClose += RestorePreviousStylesheet;
        CloseButton.OnPressed += _ => Close();

        NewSquadName.OnTextChanged += OnSquadNameTextChanged;
        SetupDebugDepartmentDropdown();

        CreateSquadButton.Disabled = string.IsNullOrWhiteSpace(NewSquadName.Text);
        CreateSquadButton.OnPressed += _ =>
        {
            if (!string.IsNullOrWhiteSpace(NewSquadName.Text))
            {
                var squadName = FormattedMessage.RemoveMarkupPermissive(NewSquadName.Text);
                if (squadName.Length > 16) squadName = squadName[..16];

                OnCreateSquad?.Invoke(squadName, DebugSquadDepartmentDropdown.Visible ? _selectedDebugDepartment : string.Empty);
                NewSquadName.Text = string.Empty;
                CreateSquadButton.Disabled = true;
            }
        };
    }

    private void SetupDebugDepartmentDropdown()
    {
        DebugSquadDepartmentDropdown.Clear();
        _debugDepartmentIds.Clear();

        foreach (var (title, department) in DebugSquadDepartments)
        {
            var index = DebugSquadDepartmentDropdown.ItemCount;
            DebugSquadDepartmentDropdown.AddItem(title);
            _debugDepartmentIds[index] = department;
        }

        DebugSquadDepartmentDropdown.Select(0);
        DebugSquadDepartmentDropdown.OnItemSelected += args =>
        {
            DebugSquadDepartmentDropdown.SelectId(args.Id);
            if (_debugDepartmentIds.TryGetValue(args.Id, out var department))
                _selectedDebugDepartment = department;
        };
    }

    protected override DragMode GetDragModeFor(Vector2 relativeMousePos) => DragMode.Move;

    protected override void FrameUpdate(FrameEventArgs args)
    {
        base.FrameUpdate(args);

        var stationTime = _gameTiming.CurTime.Subtract(_gameTicker.RoundStartTimeSpan);
        StationLabel.Text = Loc.GetString("sec-apartment-ui-mark", ("time", stationTime.ToString("hh\\:mm\\:ss")));
    }

    private void SetupAllStyles()
    {
        SetupBasicStyles();
        SetupTabContainer();
        ApplyThemeChrome();
    }

    private void SetupBasicStyles()
    {
        var theme = _styles.Theme;

        FactionLabel.FontColorOverride = theme.SubText;
        StationLabel.FontColorOverride = theme.Heading;
        StationLabel.FontOverride = _styles.GetBoldFont(16);

        UnassignedCountLabel.FontColorOverride = theme.Text;
        UnassignedCountLabel.FontOverride = _styles.GetRegularFont(13);

        SquadsCountLabel.FontColorOverride = theme.Text;
        SquadsCountLabel.FontOverride = _styles.GetRegularFont(13);

        TimersCountLabel.FontColorOverride = theme.Text;
        TimersCountLabel.FontOverride = _styles.GetRegularFont(13);

        Footer.FontColorOverride = theme.SubText;
        Footer.FontOverride = _styles.GetRegularFont(11);

        StationLabel.AddStyleClass(SecApartmentStyles.StyleClassConsoleHeading);
        NewSquadName.AddStyleClass(SecApartmentStyles.StyleClassConsoleLineEdit);
        CreateSquadButton.AddStyleClass(SecApartmentStyles.StyleClassButtonRed);

        ApplyStylesheetRules();
    }

    // Forge-Change-start
    private void ApplyStylesheetRules()
    {
        var theme = _styles.Theme;

        var buttonStyleRule = _styles.CreateButtonRedRule(
            _styles.GetButtonRedStyle(),
            _styles.GetBoldFont(11),
            theme.TabActive,
            theme.TabInactive
        );

        var lineEditRule = _styles.CreateLineEditRule(
            _styles.GetLineEditStyle(),
            _styles.GetRegularFont(11),
            theme.Text,
            theme.Placeholder
        );

        var optionRule = _styles.CreateOptionButtonRule(
            _styles.GetOptionButtonStyle(),
            _styles.GetRegularFont(11),
            theme.Text,
            theme.TabInactive
        );

        UserInterfaceManager.Stylesheet = CreateCombinedStylesheet(new[] { buttonStyleRule, lineEditRule, optionRule });
    }
    // Forge-Change-end

    private void RestorePreviousStylesheet()
    {
        UserInterfaceManager.Stylesheet = _previousStylesheet;
    }

    private void SetupTabContainer()
    {
        Tabs.SetTabTitle(0, Loc.GetString("sec-apartment-tab-squads"));
        Tabs.SetTabTitle(1, Loc.GetString("sec-apartment-tab-timers"));

        var theme = _styles.Theme;

        Tabs.TabFontColorOverride = theme.TabActive;
        Tabs.TabFontColorInactiveOverride = theme.TabInactive;

        var tabRule = _styles.CreateTabContainerRule(
            _styles.GetTabActiveStyle(),
            _styles.GetTabInactiveStyle(),
            _styles.GetPanelStyle(),
            _styles.GetBoldFont(),
            theme.TabActive,
            theme.TabInactive
        );

        Tabs.Stylesheet = CreateCombinedStylesheet(new[] { tabRule });
    }

    private Stylesheet CreateCombinedStylesheet(StyleRule[] additionalRules)
    {
        var existingStyles = _previousStylesheet ?? _ui.Stylesheet;
        var combinedRules = new List<StyleRule>();

        if (existingStyles?.Rules != null)
            combinedRules.AddRange(existingStyles.Rules);

        combinedRules.AddRange(additionalRules);

        return new Stylesheet(combinedRules);
    }

    private void OnSquadNameTextChanged(LineEdit.LineEditEventArgs args)
    {
        CreateSquadButton.Disabled = string.IsNullOrWhiteSpace(NewSquadName.Text);
    }

    private string GetFactionLabel(string departmentId)
    {
        if (_prototypeManager.TryIndex<DepartmentPrototype>(departmentId, out var department))
            return Loc.GetString(department.Name);

        return Loc.GetString("sec-apartment-unknown");
    }

    public void UpdateState(SecApartmentUpdateState state)
    {
        _factionLabel = GetFactionLabel(state.Department);
        FactionLabel.Text = _factionLabel;
        DebugSquadDepartmentDropdown.Visible = state.Debug;

        UpdateUnassignedList(state.UnassignedSecurity, state.Squads);
        UpdateSquadsList(state.Squads, state.Debug);
    }

    // Forge-Change-start
    public void ApplyUiTheme(ProtoId<SecApUiThemePrototype> uiThemeId)
    {
        if (_activeUiTheme == uiThemeId)
            return;

        if (!_prototypeManager.TryIndex(uiThemeId, out var theme))
            theme = _prototypeManager.Index<SecApUiThemePrototype>(SecApartmentComponent.DefaultUiTheme);

        _activeUiTheme = uiThemeId;
        _styles.SetTheme(theme);

        ApplyLogo(theme);
        Footer.Visible = theme.ShowFooter;

        SetupAllStyles();
    }

    private void ApplyLogo(SecApUiThemePrototype theme)
    {
        if (theme.ShowLogo && !string.IsNullOrEmpty(theme.LogoTexture))
        {
            Logo.Texture = _resCache.GetTexture(theme.LogoTexture);
            Logo.Visible = true;
        }
        else
        {
            Logo.Visible = false;
        }
    }

    private void ApplyThemeChrome()
    {
        var theme = _styles.Theme;

        Background.ModulateSelfOverride = theme.WindowBackground;
        Border.ModulateSelfOverride = theme.WindowBorder;
        CloseButton.ModulateSelfOverride = theme.CloseButton;
        ContentBorder.ModulateSelfOverride = theme.ContentBorder;
        ContentBackground.ModulateSelfOverride = theme.ContentBackground;

        SetThemedPanel(MainDivider, theme.Divider, theme.Divider, 0);
        SetThemedPanel(UnassignedListPanel, theme.ListPanelBackground, theme.ListPanelBorder);
        SetThemedPanel(CreateSquadPanel, theme.CreateSquadPanelBackground, theme.CreateSquadPanelBorder);
        SetThemedPanel(SquadsListPanel, theme.ListPanelBackground, theme.ListPanelBorder);
        SetThemedPanel(TimersListPanel, theme.ListPanelBackground, theme.ListPanelBorder);

        OsStripeSection.Visible = theme.ShowOsStripe;
        ApplyThemeToLabels(this);
    }

    private void SetThemedPanel(PanelContainer panel, Color background, Color border, int borderThickness = 2)
    {
        panel.PanelOverride = new StyleBoxFlat
        {
            BackgroundColor = background,
            BorderColor = border,
            BorderThickness = new Thickness(borderThickness)
        };

        // AngleRect stylesheet modulates panels to #292929 — washes out light palettes.
        if (_styles.Theme.ID == "SecApUiThemeUSSP")
            panel.ModulateSelfOverride = Color.White;
    }

    private void ApplyThemeToLabels(Control parent)
    {
        var theme = _styles.Theme;

        foreach (var child in parent.Children)
        {
            if (child is Label label && label != FactionLabel && label != StationLabel && label != UnassignedCountLabel
                && label != SquadsCountLabel && label != TimersCountLabel && label != Footer)
            {
                if (label.StyleClasses.Contains("ConsoleSubHeading") || label.StyleClasses.Contains("LabelSubText")
                    || label.StyleClasses.Contains("ConsoleSubText"))
                {
                    label.FontColorOverride = theme.SubText;
                }
                else if (label.StyleClasses.Contains("ConsoleHeading")
                         || label.StyleClasses.Contains("ConsoleHeadingBig"))
                {
                    label.FontColorOverride = theme.Heading;
                }
                else if (label.StyleClasses.Contains("ConsoleText"))
                {
                    label.FontColorOverride = theme.Text;
                }
                else
                {
                    label.FontColorOverride = theme.SubText;
                }
            }

            ApplyThemeToLabels(child);
        }
    }
