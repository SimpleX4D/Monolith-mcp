    private void UpdateUnassignedList(List<CrewMemberInfo> unassigned, List<Squad> squads)
    {
        _lastUnassignedCrew = unassigned;
        _lastSquads = squads;
        UnassignedCountLabel.Text = unassigned.Count.ToString();
        UnassignedContainer.Children.Clear();

        var alive = unassigned
            .Where(member => member.SensorStatus?.IsAlive == true)
            .ToList();
        var dead = unassigned
            .Where(member => member.SensorStatus?.IsAlive != true)
            .ToList();

        AddUnassignedCategory(
            Loc.GetString("sec-apartment-ui-unassigned-alive"),
            alive,
            squads,
            _showAliveUnassigned,
            expanded => _showAliveUnassigned = expanded);

        AddUnassignedCategory(
            Loc.GetString("sec-apartment-ui-unassigned-dead"),
            dead,
            squads,
            _showDeadUnassigned,
            expanded => _showDeadUnassigned = expanded);

    }

    private void AddUnassignedCategory(
        string title,
        List<CrewMemberInfo> members,
        List<Squad> squads,
        bool expanded,
        Action<bool> setExpanded)
    {
        var categoryContainer = new BoxContainer
        {
            Orientation = BoxContainer.LayoutOrientation.Vertical,
            HorizontalExpand = true,
            Margin = new Thickness(0, 0, 0, 5)
        };

        var header = new Button
        {
            Text = $"{(expanded ? "[-]" : "[+]")} {title} ({members.Count})",
            HorizontalExpand = true,
            TextAlign = Label.AlignMode.Left
        };
        header.AddStyleClass(SecApartmentStyles.StyleClassButtonRed);
        header.OnPressed += _ =>
        {
            setExpanded(!expanded);
            UpdateUnassignedList(_lastUnassignedCrew, _lastSquads);
        };

        categoryContainer.AddChild(header);

        if (expanded)
        {
            foreach (var member in members)
                categoryContainer.AddChild(CreateUnassignedCrewEntry(member, squads));
        }

        UnassignedContainer.AddChild(categoryContainer);
    }

    private CrewMemberEntry CreateUnassignedCrewEntry(CrewMemberInfo member, List<Squad> squads)
    {
        SpriteSpecifier? iconSpecifier = null;
        if (_prototypeManager.HasIndex<JobIconPrototype>(member.JobIcon))
            iconSpecifier = _prototypeManager.Index<JobIconPrototype>(member.JobIcon).Icon;
        else if (_prototypeManager.HasIndex<StatusIconPrototype>(member.JobIcon))
            iconSpecifier = _prototypeManager.Index<StatusIconPrototype>(member.JobIcon).Icon;

        var entry = new CrewMemberEntry(member, squads, iconSpecifier, _sprite, _styles);
        entry.OnAssignPressed += squadId =>
            OnAddMemberToSquad?.Invoke(squadId, member.MemberId);
        return entry;
    }

    private void UpdateSquadsList(List<Squad> squads, bool debug)
    {
        _nameDrafts = _squadEntries
            .Where(pair => pair.Value.IsEditingName)
            .ToDictionary(pair => pair.Key, pair => pair.Value.NameDraft);
        _descriptionDrafts = _squadEntries
            .Where(pair => pair.Value.IsEditingDescription)
            .ToDictionary(pair => pair.Key, pair => pair.Value.DescriptionDraft);

        SquadsCountLabel.Text = squads.Count.ToString();
        SquadsContainer.Children.Clear();
        _squadEntries.Clear();

        if (debug)
        {
            AddSquadCategory("TSF", "ТСФ", squads.Where(squad => squad.Department == "TSF").ToList(), debug);
            AddSquadCategory("NanoTrasen", "НаноТразен", squads.Where(IsNanoTrasenSquad).ToList(), debug);
            AddSquadCategory("CIV", "СИВ", squads.Where(IsCivilianSquad).ToList(), debug);
            AddSquadCategory("USSP", "СССП", squads.Where(IsUsspSquad).ToList(), debug);
            AddSquadCategory("Renegates", "Ренегаты", squads.Where(squad => squad.Department == "Renegates").ToList(), debug);
            return;
        }

        foreach (var squad in squads)
            AddSquadEntry(squad);
    }

    private void AddSquadCategory(string key, string title, List<Squad> squads, bool debug)
    {
        var expanded = _showDebugSquadCategories.GetValueOrDefault(key, false);
        var categoryContainer = new BoxContainer
        {
            Orientation = BoxContainer.LayoutOrientation.Vertical,
            HorizontalExpand = true,
            Margin = new Thickness(0, 0, 0, 5)
        };

        var header = new Button
        {
            Text = $"{(expanded ? "[-]" : "[+]")} {title} ({squads.Count})",
            HorizontalExpand = true,
            TextAlign = Label.AlignMode.Left
        };
        header.AddStyleClass(SecApartmentStyles.StyleClassButtonRed);
        header.OnPressed += _ =>
        {
            _showDebugSquadCategories[key] = !expanded;
            UpdateSquadsList(_lastSquads, debug);
        };

        categoryContainer.AddChild(header);

        if (expanded)
        {
            foreach (var squad in squads)
                categoryContainer.AddChild(CreateSquadEntry(squad));
        }

        SquadsContainer.AddChild(categoryContainer);
    }

    private void AddSquadEntry(Squad squad)
    {
        SquadsContainer.AddChild(CreateSquadEntry(squad));
    }

    private SquadEntry CreateSquadEntry(Squad squad)
    {
        var entry = new SquadEntry(squad, _styles, _prototypeManager, _sprite);
        entry.OnIconChanged += iconId =>
            OnChangeSquadIcon?.Invoke(squad.SquadId, iconId);
        entry.OnRenamePressed += newName =>
        {
            var cleanName = FormattedMessage.RemoveMarkupPermissive(newName);
            if (cleanName.Length > 16) cleanName = cleanName[..16];

            OnRenameSquad?.Invoke(squad.SquadId, cleanName);
        };
        entry.OnDeletePressed += () =>
            OnDeleteSquad?.Invoke(squad.SquadId);
        entry.OnUpdateDescriptionPressed += description =>
            OnUpdateSquadDescription?.Invoke(squad.SquadId, description);
        entry.OnStatusChanged += status =>
            OnChangeSquadStatus?.Invoke(squad.SquadId, status);
        entry.OnRemoveMemberPressed += memberId =>
            OnRemoveMemberFromSquad?.Invoke(squad.SquadId, memberId);

        _squadEntries[squad.SquadId] = entry;

        foreach (var member in squad.Members)
            _lastCrewData[member.MemberId] = member;

        if (_nameDrafts.TryGetValue(squad.SquadId, out var nameDraft))
            entry.RestoreNameEdit(nameDraft);

        if (_descriptionDrafts.TryGetValue(squad.SquadId, out var draft))
            entry.RestoreDescriptionEdit(draft);

        return entry;
    }

    private static bool IsNanoTrasenSquad(Squad squad)
    {
        return squad.Department is "Security" or "NanoTrasen" or "Command";
    }

    private static bool IsCivilianSquad(Squad squad)
    {
        return squad.Department is "CIV" or "Empire";
    }

    private static bool IsUsspSquad(Squad squad)
    {
        return squad.Department is "USSP" or "СССП" or "UnionOfSovietSocialistPlanets";
    }

    private void UpdateTimersList(List<TimerEntry> timers)
    {
        TimersCountLabel.Text = timers.Count.ToString();

        var toRemove = new List<NetEntity>();
        foreach (var (timerUid, control) in _timerControls)
        {
            if (!timers.Any(t => t.TimerUid == timerUid))
                toRemove.Add(timerUid);
        }

        foreach (var timerUid in toRemove)
        {
            if (_timerControls.TryGetValue(timerUid, out var control))
            {
                TimersContainer.Children.Remove(control);
                _timerControls.Remove(timerUid);
            }
        }

        foreach (var timer in timers)
        {
            if (!_timerControls.TryGetValue(timer.TimerUid, out var control))
            {
                control = new TimerEntryControl(timer, _styles);
                control.OnRemovePressed += () =>
                    OnRemoveTimer?.Invoke(timer.TimerUid);
                TimersContainer.AddChild(control);
                _timerControls[timer.TimerUid] = control;
            }
            else
            {
                control.UpdateTimer(timer);
            }
        }

        var sortedChildren = TimersContainer.Children
            .OfType<TimerEntryControl>()
            .OrderBy(t => t.RemainingTime.TotalSeconds)
            .ToList();

        TimersContainer.Children.Clear();
        foreach (var child in sortedChildren)
        {
            TimersContainer.AddChild(child);
        }
    }
